// @desc    Assign investigator to case
// @route   PUT /api/cases/:id/assign
exports.assignInvestigator = async (req, res) => {
  try {
    const { investigatorId } = req.body;
    
    if (!investigatorId) {
      return res.status(400).json({
        success: false,
        message: 'Please provide investigator ID'
      });
    }

    const caseData = await Case.findById(req.params.id);

    if (!caseData) {
      return res.status(404).json({
        success: false,
        message: 'Case not found'
      });
    }

    // Verify investigator exists and has correct role
    const investigator = await User.findById(investigatorId);
    if (!investigator || !['investigator', 'admin'].includes(investigator.role)) {
      return res.status(400).json({
        success: false,
        message: 'Invalid investigator'
      });
    }

    // Assign investigator
    caseData.assignedInvestigator = investigatorId;
    
    // Add to status history
    caseData.statusHistory.push({
      status: caseData.status,
      changedBy: req.user.id,
      timestamp: new Date(),
      notes: `Investigator ${investigator.name} assigned to case`
    });

    // If case is submitted, move to under_review
    if (caseData.status === 'submitted') {
      caseData.status = 'under_review';
      caseData.statusHistory.push({
        status: 'under_review',
        changedBy: req.user.id,
        timestamp: new Date(),
        notes: 'Case moved to under review after investigator assignment'
      });
    }

    await caseData.save();

    // Populate for response
    await caseData.populate('assignedInvestigator', 'name email');
    await caseData.populate('user', 'name email');

    // Notify the investigator
    await Notification.create({
      user: investigatorId,
      title: 'New Case Assigned',
      message: `You have been assigned to case "${caseData.title}" (${caseData.caseId})`,
      type: 'case_update',
      relatedCase: caseData._id,
      priority: 'high'
    });

    // Notify the user
    await Notification.create({
      user: caseData.user,
      title: 'Investigator Assigned',
      message: `${investigator.name} has been assigned to your case "${caseData.title}"`,
      type: 'case_update',
      relatedCase: caseData._id
    });

    res.status(200).json({
      success: true,
      data: caseData,
      message: 'Investigator assigned successfully'
    });
  } catch (error) {
    console.error('Assign investigator error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message
    });
  }
};

// @desc    Update case status
// @route   PUT /api/cases/:id/status
exports.updateCaseStatus = async (req, res) => {
  try {
    const { status, notes } = req.body;

    if (!status) {
      return res.status(400).json({
        success: false,
        message: 'Please provide status'
      });
    }

    // Validate status
    const validStatuses = [
      'submitted', 'under_review', 'evidence_verification',
      'investigation', 'escalated', 'resolved', 'closed'
    ];

    if (!validStatuses.includes(status)) {
      return res.status(400).json({
        success: false,
        message: 'Invalid status'
      });
    }

    const caseData = await Case.findById(req.params.id);

    if (!caseData) {
      return res.status(404).json({
        success: false,
        message: 'Case not found'
      });
    }

    // Check authorization
    if (req.user.role === 'user' && caseData.user.toString() !== req.user.id) {
      return res.status(403).json({
        success: false,
        message: 'Not authorized to update this case'
      });
    }

    // Update status
    const oldStatus = caseData.status;
    caseData.status = status;
    
    // Add to status history
    caseData.statusHistory.push({
      status: status,
      changedBy: req.user.id,
      timestamp: new Date(),
      notes: notes || `Status changed from ${oldStatus} to ${status}`
    });

    await caseData.save();

    // Notify the user about status change
    await Notification.create({
      user: caseData.user,
      title: 'Case Status Updated',
      message: `Your case "${caseData.title}" status changed from ${oldStatus.replace(/_/g, ' ')} to ${status.replace(/_/g, ' ')}`,
      type: 'case_update',
      relatedCase: caseData._id
    });

    // Also notify investigator if assigned
    if (caseData.assignedInvestigator && caseData.assignedInvestigator.toString() !== req.user.id) {
      await Notification.create({
        user: caseData.assignedInvestigator,
        title: 'Case Status Updated',
        message: `Case "${caseData.title}" status changed to ${status.replace(/_/g, ' ')}`,
        type: 'case_update',
        relatedCase: caseData._id
      });
    }

    res.status(200).json({
      success: true,
      data: caseData,
      message: 'Case status updated successfully'
    });
  } catch (error) {
    console.error('Update status error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message
    });
  }
};